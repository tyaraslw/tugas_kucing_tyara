  <h1><u>Kucing</u></h1>
    <img src="kucing1.jpg" width="400" height="250" /><br>
    <br>
    <b>Kucing</b> disebut juga <b>kucing domestik</b><font color="blue" size="1"><sup>[4][5]</font> atau <b>kucing rumah</b> (<font color="blue">nama ilmiah</font>: <i>Felis silvestris catus</i> atau <i>Felis<br>
    catus</i>) adalah sejenis <font color="blue">mamalia karnivora</font> dari keluarga Felidae. Kata "kucing" biasanya merujuk kepada <br>
    "kucing" yang telah dijinakkan,<font color="blue" size="1"><sup>[6]</font> tetapi bisa juga bisa merujuk kepada "<font color="blue">kucing besar</font>" seperti <font color="blue">singa</font> dan <br>
    <font color="blue">harimau</font> yang juga termasuk jenis kucing.<br>
    <br>
    Kucing telah berbaur dengan kehidupan <font color="blue">manusia</font> paling tidak sejak 5.000 tahun SM, dengan ditemukannya <br> 
    <font color="blue">kerangka</font> kucing di <font color="blue">Pulau Siprus</font>.<font color="blue" size="1"><sup>[7]</font> Sejak zaman 3.500 SM, orang <font color="blue">Mesir Kuno</font> telah menggunakan kucing <br>
    untuk menjauhkan <font color="blue">tikus</font> atau <font color="blue">hewan pengerat</font> lain dari <font color="blue">lumbung</font> yang menyimpan hasil panen.<font color="blue" size="1"><sup>[8]</font><br>
    <br>
    Saat ini, kucing adalah salah satu <font color="blue">hewan peliharaan</font> terpopuler di dunia.<font color="blue" size="1"><sup>[9]</font> Kucing yang garis keturunannya <br>
    tercatat secara resmi sebagai kucing <font color="blue">trah</font> atau <font color="blue">galur murni</font> (<i>pure breed</i>), seperti <font color="blue">persia</font>, <font color="blue">siam</font>, <font color="blue">manx</font>, dan <br>
    <font color="blue">sphinx</font>. Kucing seperti ini biasanya dibiakkan di tempat pemeliharaan hewan resmi. Jumlah kucing ras <br>
    hanyalah 1% dari seluruh kucing di dunia, sisanya adalah kucing dengan keturunan campuran, seperti <font color="blue">kucing <br>
    liar</font> atau kucing kampung. Kucing juga merupakan jenis hewan peliharaan yang banyak diminati karena <br>
    tingkah lakunya yang lucu. <br> 
