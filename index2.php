<h1><u>Kucing besar</u></h1>
  <img src="kucing2.jpg" width="400" height="260" /><br>
  <br>
  Istilah <b>kucing besar</b> - yang bukan merupakan klasifikasi biologis - digunakan untuk membedakan spesies kucing <br>
  yang lebih besar dari yang lebih kecil. Salah satu definisi "kucing besar" termasuk empat anggota genus Panthera: <br>
  <font color="blue">harimau</font>, <font color="blue">singa</font>, <font color="blue">jaguar</font>, dan <font color="blue">macan tutul</font>. Anggota dari genus ini adalah satu-satunya kucing bisa mengaum. <br>
  Definisi yang lebih luas dari "kucing besar" juga mencakup <font color="blue">cheetah</font>, <font color="blue">macan tutul salju</font>, dan <font color="blue">puma</font>. <br>
  Meskipun perbedaan besar dalam ukuran, berbagai jenis kucing yang sangat mirip dalam struktur dan perilaku <br> 
  baik, dengan pengecualian cheetah, yang secara signifikan berbeda dari semua kucing yang besar ataupun kecil. <br> 
  Mereka bisa ditemukan di Amerika, Afrika, Asia, dan Eropa.
